	<h3>Latest press release: JPPF 1.9.1</h3>
	<p>This version brings bug fixes and increased stability, along with a few new features.
	<p>Numerous bugs have been fixed in this maintenance version, increasing the stability and reliability of the platform components.
	<p>Improvements and fixes have been brought to the management, client and server components, as well as to the J2EE and GigaSpaces connectors.
